import {goalsApi} from "@/services/goalsService.ts";

const goalsApiSlice = goalsApi.injectEndpoints({
    endpoints: (builder) => ({
        getGoalsHeadersByUserId: builder.query({
            query: (userId) => ({
                url: `/goals/getAllGoalsHeaders/${userId}`,
                method: "GET",
            }),
            providesTags: (result) =>
                ["goalsHeaders"]
        }),
        addGoal: builder.mutation({
            query: ({userId, title}) => ({
                url: `/goals/addGoal/${userId}`,
                method: "PUT",
                body: {
                    title
                },
            }),
            invalidatesTags: ["goalsHeaders"],
        }),
        deleteGoal: builder.mutation({
            query: (goalId) => ({
                url: `/goals/deleteGoal/${goalId}`,
                method: "DELETE",
            }),
            invalidatesTags: ["goalsHeaders"],
        }),
    }),
});

// Export hooks generated by RTK Query for use in components
export const {
    useGetGoalsHeadersByUserIdQuery,
    useAddGoalMutation,
    useDeleteGoalMutation,
} = goalsApiSlice;